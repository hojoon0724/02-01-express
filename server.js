// -----------------------------------------------------
// Dependencies
// -----------------------------------------------------
const express = require("express");

// -----------------------------------------------------
// Application Object
// -----------------------------------------------------
const app = express();

// -----------------------------------------------------
// Middleware
// -----------------------------------------------------

// -----------------------------------------------------
// Routes
// -----------------------------------------------------

// -----------------------------------------------------
// GET requests
// -----------------------------------------------------
app.get("/", (req, res) => {
  res.send(`root response`);
});

app.get("/greeting/:name", (req, res) => {
  const name = req.params.name;
  res.send(`<p>Hello! ${name}</p>`);
});

// -----------------------------------------------------
// Listener
// -----------------------------------------------------
app.listen(3000, () => {
  console.log(`listening in port 3000`);
});
